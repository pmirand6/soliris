CREATE PROCEDURE `ST_LIST_APM_ACTIVOS`()
    COMMENT 'Lista los APM activos'
BEGIN

  SELECT
    a.*,
    m.valor AS estado
  FROM apm a
    INNER JOIN maestro_estado m
      ON m.id = a.estado_id
  WHERE a.estado_id = 17
  ORDER BY a.nombre_completo DESC;

